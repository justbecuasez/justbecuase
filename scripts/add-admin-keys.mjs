function countLeafKeys(obj) {
  let count = 0;
  for (const key in obj) {
    if (typeof obj[key] === 'object' && obj[key] !== null) {
      count += countLeafKeys(obj[key]);
    } else {
      count++;
    }
  }
  return count;
}

